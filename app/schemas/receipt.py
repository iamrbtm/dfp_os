from __future__ import annotations

from marshmallow import Schema, fields


class ReceiptSchema(Schema):
    id = fields.Integer(dump_only=True)
    user_id = fields.Integer()
    status = fields.String()
    original_file_id = fields.String(allow_none=True)
    preview_file_id = fields.String(allow_none=True)
    thumbnail_file_id = fields.String(allow_none=True)
    source_type = fields.String()
    file_hash = fields.String(allow_none=True)
    raw_ocr_text = fields.String(allow_none=True)
    raw_ocr_json = fields.String(allow_none=True)
    ai_extracted_json = fields.String(allow_none=True)
    parser_provider = fields.String(allow_none=True)
    parser_model = fields.String(allow_none=True)
    confidence_overall = fields.Decimal(as_string=True, allow_none=True)
    low_confidence_flags = fields.String(allow_none=True)
    merchant_name = fields.String(allow_none=True)
    store_name = fields.String(allow_none=True)
    store_number = fields.String(allow_none=True)
    address_line_1 = fields.String(allow_none=True)
    address_line_2 = fields.String(allow_none=True)
    city = fields.String(allow_none=True)
    state = fields.String(allow_none=True)
    postal_code = fields.String(allow_none=True)
    phone = fields.String(allow_none=True)
    receipt_number = fields.String(allow_none=True)
    transaction_number = fields.String(allow_none=True)
    date_time = fields.DateTime(dump_only=True, allow_none=True)
    timezone = fields.String(allow_none=True)
    subtotal = fields.Decimal(as_string=True, allow_none=True)
    tax_total = fields.Decimal(as_string=True, allow_none=True)
    fee_total = fields.Decimal(as_string=True, allow_none=True)
    discount_total = fields.Decimal(as_string=True, allow_none=True)
    tip_total = fields.Decimal(as_string=True, allow_none=True)
    deposit_total = fields.Decimal(as_string=True, allow_none=True)
    rounding_adjustment = fields.Decimal(as_string=True, allow_none=True)
    grand_total = fields.Decimal(as_string=True, allow_none=True)
    payment_method = fields.String(allow_none=True)
    payment_card_brand = fields.String(allow_none=True)
    payment_card_last4 = fields.String(allow_none=True)
    currency = fields.String()
    duplicate_group_id = fields.Integer(allow_none=True)
    duplicate_score = fields.Decimal(as_string=True, allow_none=True)
    approved_at = fields.DateTime(dump_only=True, allow_none=True)
    approved_by_id = fields.Integer(allow_none=True)
    rejected_at = fields.DateTime(dump_only=True, allow_none=True)
    rejected_by_id = fields.Integer(allow_none=True)
    notes = fields.String(allow_none=True)
    created_at = fields.DateTime(dump_only=True)
    updated_at = fields.DateTime(dump_only=True)


class ReceiptLineItemSchema(Schema):
    id = fields.Integer(dump_only=True)
    receipt_id = fields.Integer()
    row_order = fields.Integer()
    raw_text = fields.String(allow_none=True)
    description = fields.String(allow_none=True)
    normalized_description = fields.String(allow_none=True)
    sku = fields.String(allow_none=True)
    upc = fields.String(allow_none=True)
    quantity = fields.Decimal(as_string=True, allow_none=True)
    unit_of_measure = fields.String(allow_none=True)
    unit_price = fields.Decimal(as_string=True, allow_none=True)
    line_subtotal = fields.Decimal(as_string=True, allow_none=True)
    line_discount = fields.Decimal(as_string=True, allow_none=True)
    line_tax = fields.Decimal(as_string=True, allow_none=True)
    line_fee = fields.Decimal(as_string=True, allow_none=True)
    line_deposit = fields.Decimal(as_string=True, allow_none=True)
    line_tip_allocation = fields.Decimal(as_string=True, allow_none=True)
    line_total = fields.Decimal(as_string=True, allow_none=True)
    taxable_status = fields.String()
    needs_review = fields.Boolean()
    is_inventory_candidate = fields.Boolean()
    is_personal_or_excluded = fields.Boolean()
    notes = fields.String(allow_none=True)
    created_at = fields.DateTime(dump_only=True)
    updated_at = fields.DateTime(dump_only=True)


class ReceiptLineAllocationSchema(Schema):
    id = fields.Integer(dump_only=True)
    receipt_line_item_id = fields.Integer()
    allocation_type = fields.String()
    market_id = fields.Integer(allow_none=True)
    custom_job_id = fields.Integer(allow_none=True)
    inventory_item_id = fields.Integer(allow_none=True)
    expense_category_id = fields.Integer(allow_none=True)
    amount = fields.Decimal(as_string=True, allow_none=True)
    percent = fields.Decimal(as_string=True, allow_none=True)
    quantity_allocated = fields.Decimal(as_string=True, allow_none=True)
    notes = fields.String(allow_none=True)


class ReceiptDashboardSchema(Schema):
    total_this_month = fields.Integer()
    needs_review = fields.Integer()
    possible_duplicates = fields.Integer()
    approved_total = fields.Decimal(as_string=True)
    unallocated = fields.Integer()
    recent = fields.List(fields.Nested(ReceiptSchema))
    vendors = fields.List(fields.Dict)
