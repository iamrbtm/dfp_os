from __future__ import annotations

from marshmallow import Schema, fields


class MarketSchema(Schema):
    id = fields.Integer(dump_only=True)
    name = fields.String(required=True)
    location_name = fields.String(allow_none=True)
    address = fields.String(allow_none=True)
    city = fields.String(allow_none=True)
    state = fields.String(allow_none=True)
    zip_code = fields.String(allow_none=True)
    event_date = fields.Date(allow_none=True)
    start_time = fields.String(allow_none=True)
    end_time = fields.String(allow_none=True)
    latitude = fields.Float(allow_none=True)
    longitude = fields.Float(allow_none=True)
    application_submitted_at = fields.DateTime(allow_none=True)
    application_approved_at = fields.DateTime(allow_none=True)
    fee_paid_at = fields.DateTime(allow_none=True)
    booth_location = fields.String(allow_none=True)
    booth_size = fields.String(allow_none=True)
    power_available = fields.Boolean(allow_none=True)
    wifi_available = fields.Boolean(allow_none=True)
    food_available = fields.Boolean(allow_none=True)
    load_in_at = fields.DateTime(allow_none=True)
    load_out_at = fields.DateTime(allow_none=True)
    load_in_notes = fields.String(allow_none=True)
    load_out_notes = fields.String(allow_none=True)
    booth_fee = fields.Decimal(as_string=True, allow_none=True)
    application_fee = fields.Decimal(as_string=True, allow_none=True)
    status = fields.String(required=True)
    expected_traffic = fields.String(allow_none=True)
    actual_revenue = fields.Decimal(as_string=True, allow_none=True)
    actual_profit = fields.Decimal(as_string=True, allow_none=True)
    notes = fields.String(allow_none=True)
    created_at = fields.DateTime(dump_only=True)
    updated_at = fields.DateTime(dump_only=True)


